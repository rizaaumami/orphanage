package com.riza.orphanage;

public class Input {
    private String id;
    private String nama_panti;
    private String tentang_panti;
    private String alamat_panti;
    private String foto_panti;
    private String telephone_panti;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNama_panti() {
        return nama_panti;
    }

    public void setNama_panti(String nama_panti) {
        this.nama_panti = nama_panti;
    }

    public String getTentang_panti() {
        return tentang_panti;
    }

    public void setTentang_panti(String tentang_panti) {
        this.tentang_panti = tentang_panti;
    }

    public String getAlamat_panti() {
        return alamat_panti;
    }

    public void setAlamat_panti(String alamat_panti) {
        this.alamat_panti = alamat_panti;
    }

    public String getFoto_panti() {
        return foto_panti;
    }

    public void setFoto_panti(String foto_panti) {
        this.foto_panti = foto_panti;
    }

    public String getTelephone_panti() {
        return telephone_panti;
    }

    public void setTelephone_panti(String telephone_panti) {
        this.telephone_panti = telephone_panti;
    }
}
