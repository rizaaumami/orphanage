package com.riza.orphanage;

public class Panti {
    private String id;
    private String nama_panti;
    private String lokasi;
    private String tentang_panti;
    private String telephone;
    private String link_gambar;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNama_panti() {
        return nama_panti;
    }

    public void setNama_panti(String nama_panti) {
        this.nama_panti = nama_panti;
    }

    public String getLokasi() {
        return lokasi;
    }

    public void setLokasi(String lokasi) {
        this.lokasi = lokasi;
    }

    public String getTentang_panti() {
        return tentang_panti;
    }

    public void setTentang_panti(String tentang_panti) {
        this.tentang_panti = tentang_panti;
    }

    public String getTelephone() {
        return telephone;
    }

    public void setTelephone(String telephone) {
        this.telephone = telephone;
    }

    public String getLink_gambar() {
        return link_gambar;
    }

    public void setLink_gambar(String link_gambar) {
        this.link_gambar = link_gambar;
    }
}
